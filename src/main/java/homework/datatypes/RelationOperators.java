package homework.datatypes;

public class RelationOperators {
    public static void main(String [] args){
        int a = 10;
        int b = 30;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        //it look all answer it right.

    }
}
